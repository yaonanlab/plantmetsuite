library(mailR)
library(readxl)
args <- commandArgs(T)
source("/srv/myapps/keyandaydayup/email.R",encoding = "UTF-8")
taskidnow<-args[1]
app20_email<-args[2]
taskidnow<-as.character(taskidnow)
app20_email <- as.character(app20_email)
filename1<-list.files(file.path("/srv/myapps/keyandaydayup/userdata",taskidnow,"rawdata"),pattern="1",full.names=T)
outname1<-file.path("/srv/myapps/keyandaydayup/userdata",taskidnow,"result","result.fastq")
outname2<-file.path("/srv/myapps/keyandaydayup/userdata",taskidnow,"result","result.html")
outname3<-file.path("/srv/myapps/keyandaydayup/userdata",taskidnow,"result")
mycmd1<-paste("/srv/myapps/keyandaydayup/scripts/app20/fastp -i ",filename1," -o ",outname1," -z 4 -q 20 -u 30 -n 10  -h ",outname2)
mycmd2<-paste("cp /fastp.json -f ",outname3)
system(mycmd1)
system(mycmd2)
finish<-"Job finished"
write.table(finish,file.path("/srv/myapps/keyandaydayup/userdata",taskidnow,"result","Finish.txt"),row.names=F,col.names=F,quote=F)
if(app20_email!=""&("TRUE"%in%grepl("@",app20_email))){
setwd("/srv/myapps/keyandaydayup/")
tar(file.path("userdata",taskidnow,"result.zip"),files=file.path("userdata",taskidnow,"result"))
attach.file <- file.path("/srv/myapps/keyandaydayup/userdata",taskidnow,"result.zip")
sendmail(to=as.character(app20_email),attach=T,attach.file=attach.file)
}
